package smallStudyClass.methodcreation_methodoeverloading2802;

public class Calculator {

    // Let's create a method that subtracts the value of two variables

    public static void subtraction(int a ,int b){
        System.out.println("the different number a and b is  :"+(a-b));
    }

    public static void add(int a ,int b){
        System.out.println("the different number a and b is  :"+(a+b));
    }

    public void product(int a ,int b){
        System.out.println("the different number a and b is  :"+(a*b));
    }

    public void divided(int a ,int b){
        System.out.println("the different number a and b is  :"+(a/b));
    }
}
